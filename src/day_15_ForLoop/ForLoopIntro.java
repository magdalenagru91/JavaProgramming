package day_15_ForLoop;

public class ForLoopIntro {
    public static void main(String[] args) {

        System.out.println("Hello World!");
        System.out.println("Hello World!");
        System.out.println("Hello World!");
        System.out.println("Hello World!");
        System.out.println("Hello World!");
        System.out.println("Hello World!");
        System.out.println("Hello World!");
        System.out.println("Hello World!");
        System.out.println("Hello World!");
        System.out.println("Hello World!");


        System.out.println("-------------------------------increased 10");

        for (int i = 1; i<=10; i++) {
           System.out.println("Hello World!");
        }

        System.out.println("-------------------------------increased 5");

        for (int i = 1; i<=10; i+=2) {
            System.out.println("Hello World!");
        }

    }
}

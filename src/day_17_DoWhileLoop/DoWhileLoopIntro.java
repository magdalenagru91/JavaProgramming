package day_17_DoWhileLoop;

public class DoWhileLoopIntro {

    public static void main(String[] args) {
        boolean a = false;
        while (a) {                     //WHILE LOOP
            System.out.println("Wooden Spoon");
        }


            System.out.println("--------------------------");       //DO WHILE LOOP

            do {
                System.out.println("Wooden Spoon - do while loop");
            } while (a);
        }

    }

